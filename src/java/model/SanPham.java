package model;

/**
 *
 * @author Back
 */
public class SanPham {
    int id;
    String ten, linkAnh, moTa;
    double gia;

    public SanPham() {
    }

    public SanPham(int id, String ten, String linkAnh, String moTa, double gia) {
        this.id = id;
        this.ten = ten;
        this.linkAnh = linkAnh;
        this.moTa = moTa;
        this.gia = gia;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getLinkAnh() {
        return linkAnh;
    }

    public void setLinkAnh(String linkAnh) {
        this.linkAnh = linkAnh;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }

    public double getGia() {
        return gia;
    }

    public void setGia(double gia) {
        this.gia = gia;
    }
    
}
